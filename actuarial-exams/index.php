           <h4>Overview of the Actuarial Exam Process</h4>
          
        <p>
            Actuaries have to take a bunch of professional exams to become fully certified. These exams are notorious for being some of the hardest professional exams to pass (comparable to the BAR and CPA). There are two main types of actuaries and hence two main actuarial societies in North America. The <a href="#">SOA</a> or Society of Actuaries deals with insurance relating to life, health and pensions. The CAS (Casualty Actuarial Society) relates to insurance around property and casualty. This includes car, home and disaster insurance. The first four exams are common to all actuarial streams and are also known as the preliminary exams. 
        </p>
            
             
        </div>
            
