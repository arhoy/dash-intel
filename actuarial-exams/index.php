          <h4 id = "prelimExams">The Preliminary Exams</h4>
          <p>
             The first four preliminary actuarial exams are:
          </p>    
           <ul>
               <li>
                   Exam P: Probability
               </li>
               <li>
                   Exam FM: Financial Mathematics
               </li>
               <li>
                   Exam MFE: Models for Financial Economics
               </li>
               <li>
                   Exam C: Construction and Evaluation of Actuarial Models
               </li><br>
        
              <p>After this the pathway splits and the exams are no longer common between each designation. Before going further decide which actuary stream resonates with you.
              
          
              
              <p> <strong>Note:</strong> Successfully passing all the preliminary actuary exams above does <strong>NOT</strong> make you an ASA (associate of the socieity of actuaries) or an associate of CAS. To do so you must meet all the <a href="#">SOA requirments </a>and <a href="#">CAS requirements</a> outlined.  </p> 
               
               
           </ul>
