         <h4 id = "examP">EXAM P: Probability</h4>
            <h5>Overview</h5>
              <p>The exam covers basic to intermediate probability concepts covered typically in a first and second year university stats class. Knowledge of first year calculus is expected. The exam is typically quite a bit harder than a university exam setting with a pass rate around 50-60%. Part of the challenge comes from the time limit. 30 questions need to be solved in 3 hours with most of the questions invovling indepth calculations on probablility or expected values. A calculator is allowed but no formula sheet. This means students must <em>memorize</em> all probablility distrubitions covered in the exam topics. A non graphing calculator is allowed and a formula sheet with z-tables and pencil are provided. Exam P by far has the shortest amount of material, yet often poses as a more challenging exam then FM to some due to the depth and difficulty of some of the questions asked.</p>
            
