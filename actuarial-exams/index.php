         <h4 id = "howMuchToStudy">How much should you study?</h4>
        
            <p>Mileage will vary depending on your background, learning ability, how fresh the material is, and how prepared you want to be and feel going into the exam. Typically, the amount of time spent studying is around 100 hours per hour of an exam. So if Exam P is 3 hours, typically spend around 300 hours studying in total. Again this will vary greatly from student to student. It is wise to plan to study well ahead of the exam. Understand concepts thoroughly rather than cram and memorize. Finally, leave time for practice exams and practice them under exam-like conditions. For more tips visit <a href="#">How to pass and actuary exam the first time</a>.</p>
            
    
              
         </div>     
                
                   
         <div class="whitebox"> 
         <h4 id = "orderOfExams">What order can you take the exams in?</h4>
        
            <p>Exam P or Exam 1 is typically the first actuarial exam taken, though any of the first four exams can be taken in any order. It is highly unusual for someone to not take Exam P or Exam FM as their first Exam since the later Exams build off the knowledge from Exam P and Exam FM. Exam P and exam FM are also offered the most frequently with a exam sitting occuring every two months. The later preliminary exams are offered every 4 months.</p>
