            <div class="whitebox">
           <h4>16. Economist</h4>
           <p><strong>The What?</strong>
           An economist often works with the government, university or R&D department. Someone with an economist job title typically holds an advanced degree in an analytical field such as economics, statistics or operations research. They are involved with research, analyses and building econometric models. An ideal economist will create and deliver statistically significant, highly relevant, customized and automated reports. They will use statistical packages such as SAS and R to create accurate industry forecasts. Further, they will use their expertise and experience to lead research projects.   
           </p>
           <p><strong>The How To:</strong>
           An advanced degree, typically Ph.D in economics or related area of research
            </p>
            </div>

            <div class="whitebox">
           <h4>17. Epidemiologist</h4>
           <p><strong>The What?</strong>
           An epidemiologist is an expert in the cause and effect relationships of diseases within human populations. They have a masters, M.D or phD in Epidemiology, public health or a related field. They use R, Python or SAS to manipulate and analyze large datasets in a R&D department or for the government. Possible employers: 23andMe, Ancestry, Centers for disease control and prevention.
           </p>
           <p><strong>The How To:</strong>
           An advanced degree, with emphasis on epidemiology or public health. Gain experience in clinical research. 
            </p>
            </div>

            <div class="whitebox">
           <h4>18.Operations Research</h4>
           <p><strong>The What?</strong>
           Operations research implies using analytical methods to make informed decisions. An operations research analyst or manager will apply mathematical models and techniques such as regression, sensitivity analysis, linear and nonlinear programming and time series to a wide range of business applications. An operations research type business problem, often start with a set of constraints (i.e time, labour hours, machines) to optimize a set goal (i.e maximize profit) over a set period of time and under uncertain conditions.  
           </p>
           <p><strong>The How To:</strong>
           A degree in a quantitative field such as mathematical sciences, statistics or operations science. 
            </p>
            </div>

            <div class="whitebox">
           <h4>19. Assurance Quality Tester</h4>
           <p><strong>The What?</strong>
           A quality assurance tester always has the end user in mind. They work closely with IT business analysts to identify and resolve bugs or defects. As an assurance quality tester, you test all use cases using software development tools to debug and maintain programs and applications. 
           </p>
           <p><strong>The How To:</strong>
           An degree in IT or related field with a strong customer/end user focused personality. 
            </p>
            </div>

            <div class="whitebox">
           <h4>20. Quant</h4>
           <p><strong>The What?</strong>
           In the most simple sense, a quant or quantitative analyst looks at information a typical investor looks at but in a systematic way. They price securities and analyze trends from the complex models they create to provide invaluable information to investment banks and hedge funds. Quants usually hold an advanced degree in a quantitative field such as financial engineering. Work is often limited to major world financial centers and positions are extremely competitive. As a job on wall street, the high stress and high pay go hand in hand. 
           </p>
           <p><strong>The How To:</strong>
           To be considered, you typically need a PhD in a quantitative field such as financial engineering, physics, math, computer science and statistics. Experience working with complex data sets and have superior programming skills. You solve problems others in your class can’t solve and you enjoy working long hours under highly stressful situations.
            </p>
