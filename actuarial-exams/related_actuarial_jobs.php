           <h4>15. Market Researcher</h4>
           <p><strong>The What?</strong>
           You collect data from various different sources, provide actionable data insights to end users, and create reports and presentations as needed. A market researcher is an effective communicator that is always learning and adapting their skills. Typically a more entry level and varying role, market researcher get exposure to different areas of a business. Overtime, a market researcher could specialize in a certain area or move onto a manager type position. 
           </p>
           <p><strong>The How To:</strong>
           A degree or diploma in a related field. 
            </p>
