           <h4>1. Business Analyst</h4>
           <p><strong>The What?</strong>
           A bridge between the tech world and the business stakeholders. This role though often less technical, incorporates strong writing and communication skills. A business analyst has a broad and deep knowledge of a company’s operations and communicates industry insights to the rest of the organization. Business analysts often work on many different projects with a varying day to day work schedule.  
           </p>
           <p><strong>The How To:</strong>
            Areas of study varies and depends on the business itself. Common education requirements include a business degree or an MBA. Companies looking to hire business analysts almost always look for candidates with at least a bachelor's degree. 
            </p>
            </div>

            <div class="whitebox">
           <h4>2. Marketing Analyst</h4>
           <p><strong>The What?</strong>
           An information detective. They often analyze and interpret large amounts of data into sensible bite sized bits through an array of graphs and other visuals. They often help with reporting needs and answer important questions around customer behaviour, buying patterns and market trends. A marketing analyst uses a wide range of tools like Python, SQL, R, Power Query and of course Excel. 
           </p>
           <p><strong>The How To:</strong>
           Analysts have a strong quantitative background. They typically hold a diploma/degree in a quantitative field such as math, economics, stats, finance, physics, engineering, business or comp sci. 
            </p>
            </div>

            <div class="whitebox">
           <h4>3. Digital Marketing Specialist</h4>
           <p><strong>The What?</strong>
           Promote the growth of a company through brand awareness. This involves advertising their products and services online through mediums such as Google AdWords and Facebook Ads. An experienced digital marketing specialist should also be able to set up targeted and automated email marketing campaigns, write effective ad copy,  generate digital growth, and analyze results through properly tracked and tested marketing campaigns. 
           </p>
           <p><strong>The How To:</strong>
           Tools of the trade include Google Analytics, Facebook Ads, Google AdWords, SEO, CSS, Html, Javascript. A degree in marketing or business is most common and some web development experience is prefered or required.  
            </p>
            </div>

            <div class="whitebox">
           <h4>4. Reporting Analyst</h4>
           <p><strong>The What?</strong>
           A broad job title that sounds quite boring. Nevertheless, this is often a critical role that includes
           <ul class="ae__nav">
           <li class="ae__item">Monthly, weekly or daily reporting on key business metrics</li>
           <li class="ae__item">Budgeting and forecasting revenues and operation costs</li>
           <li class="ae__item">Support of business analysis through the creation of dashboards</li>
           </ul>
            <p>
           A talented reporting analyst can create huge value for a business by reporting key and actionable business metrics to end users. Having the right metrics to the rights users at the right time is critical for every successful and data driven business
           </p>
           </p>
           <p><strong>The How To:</strong>
           Reporting analysts skill sets vary considerably.  In most cases, they are knowledgeable with ERP systems, SQL, and data automation in general. 
            </p>
            </div>

            <div class="whitebox">
           <h4>5. Financial Analyst:</h4>
           <p><strong>The What?</strong>
           Financial analyst often means different things to different companies. Often there is a lot of overlap between financial analyst, reporting analyst and business analyst. Typically, a financial analyst is less involved with the day to day reporting and more involved with forecasting future events. They dive deep into the financial data and use excel and other software to analyze trends, competition and price movements. 
           </p>
           <p><strong>The How To:</strong>
           Fields of study include finance, accounting, business, mathematics, engineering. Work towards obtaining your CFA (chartered financial analyst) while working as a junior financial analyst, accountant or similar position.  You should have an interest in financial markets keeping up to date with financial, political, local and global events.
            </p>
            </div>

            <div class="whitebox">
           <h4>6. Risk Management</h4>
           <p><strong>The What?</strong>
           A risk manager assess and interprets the probabilities for potential threats in the areas such as:
            </p>
            <ul class="ae__nav">
           <li class="ae__item">Work Safety</li>
           <li class="ae__item">Financial Security</li>
           <li class="ae__item">Legal Liability</li>
           </ul>
           <p>
           A risk specialist works to minimize the potential for risk, outlining and documenting safety hazards or legal and financial concerns.
           </p>
  
           <p><strong>The How To:</strong>
           Risk management parallels the actuary profession in many ways. Study a quantitative degree such as finance and obtain a job related to financial analysis or portfolio management. While gaining relevant experience, pass the FRM exams and apply for risk management jobs. 
            </p>
            </div>

            <div class="whitebox">
           <h4>7. Data Scientist</h4>
           <p><strong>The What?</strong>
           Data science and data scientist are popular buzzwords in today’s data driven world. A data scientist is not merely a sr. data analyst or business analyst with “tech skills” even though some people might think that could be the case. As a data scientist you typically hold an advanced degree (masters or phD) in computer science or another data intensive field where part of your research involved analyzing large amounts of complex data. (i.e think particle physics). Data scientists should have experience with machine learning, neural networks, computer vision and a solid foundation in advanced statistics and programming. Further, data scientist communicate findings in simple to understand terms which drive actionable business decisions.  
           </p>
           <p><strong>The How To:</strong>
           Earn a PhD in a quantitative field where you have to comb through and analyze massive data sets using advanced statistics and big data software like Hadoop. Alternatively, gain years of progressive hands on experience as a data analyst where you you apply data science techniques such as machine learning, predictive modeling to answer questions you couldn’t solve with simple data analysis techniques.  
            </p>
            </div>

            <div class="whitebox">
           <h4>8. Programmer</h4>
           <p><strong>The What?</strong>
           Being a great programmer is a lot more than just writing code. A great programmer writes clean, understandable and maintainable code. They’re  able to quickly find the right tools and technologies needed for any given project due to a deep a broad technical skill set. A great programmer keeps up to date with that latest technologies and emerging tech trends. A great  programmer uses stack overflow and libraries as tools for knowledge and efficiency. On a final note, a great programmer always keeps the end user in mind.
           </p>
           <p><strong>The How To:</strong>
           You need to think like a programmer and you need to love writing code. You cannot get easily discouraged when you cannot find the bugs in your program and you need to keep practicing and work on various projects from time to time outside of work or school. 
            </p>
            </div>

            <div class="whitebox">
           <h4>9. Data consultant</h4>
           <p><strong>The What?</strong>
           A consultant helps companies answer critical issues and solve problems members of their organization might not be the best equipped to handle.. Consultants leverage the findings and experience from working with many other businesses to help a company solve their pain points. They could be involved with all aspects of a large project or serve as guiding mentor to an organization. 
           </p>
           <p><strong>The How To:</strong>
           Gain progressive experience in a related field such as data scientist, software engineer, business analyst. Work on projects from various areas of the business and practice communicating complex topics or ideas in a clear and concise manner. Learn to art of being an effective and persuasive communicator.
            </p>
            </div>

            <div class="whitebox">
           <h4>10. Insurance Underwriter</h4>
           <p><strong>The What?</strong>
           An insurance underwriter rejects or accepts an applicant based on their risk factors taking account the price rates set by the actuaries and the competitive market. They use this knowledge and their experience to determine if they should take on the risk of insuring someone or pass up on the business opportunity. Insurance underwriters are effective communicators who are analytical and detailed. 
           </p>
           <p><strong>The How To:</strong>
           Complete your CIP (chartered insurance professional) certification. Having a degree or diploma in business, accounting, commerce, math or related field is often an asset or a requirement. 
            </p>
            </div>

            <div class="whitebox">
           <h4>11. Statistician </h4>
           <p><strong>The What?</strong>
           Although data analysts and especially data scientists must have a solid foundation in statistics, a statisticians role is typically more specialized. Often statisticians design surveys and conduct experiments and then analyze these results. They apply statistical models and methods to solve various business problems and collect data with statistical rigour. They are typically part of an R&D department and are often employed by pharmaceutical and medical companies to help conduct clinical trials. 
           </p>
           <p><strong>The How To:</strong>
           A advanced degree, typically Ph.D, in statistics or biostatistics. Gain a wealth of research experience while in academia. 
            </p>
            </div>

            <div class="whitebox">
           <h4>12. Data Architect</h4>
           <p><strong>The What?</strong>
           This IT role is becoming increasingly more important as companies collect ever increasing amounts of data. A data architect plans for the long term laying out blueprints central to data management systems and the maintenance of data sources. This difficult and highly critical job often works “behind the scenes” and is crucial to providing the business and end users clean, accurate and timely data. Essentially, a data architect is a data steward responsible for overall data governance of an organization.  
           </p>
           <p><strong>The How To:</strong>
           Degree in computing science, computer engineering, IT or related field. In addition, several years of IT experience is necessary. Completing a CDMP certificate could help with job prospects.
            </p>
            </div>

